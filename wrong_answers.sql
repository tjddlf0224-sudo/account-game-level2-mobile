--    정책은 기존 groups/scores 와 동일하게 anon(publishable 키)에 부여.
--    (교실용 앱 보안 수준에 맞춤)
--
--  재실행 주의: create table/index 는 if not exists 라 안전하지만,
--  create policy 는 이미 있으면 에러납니다. 정책 줄은 한 번만 실행.
--  (다시 돌려야 하면 각 policy 앞에
--   drop policy if exists "이름" on public.wrong_answers; 를 추가)
