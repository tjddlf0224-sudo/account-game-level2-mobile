create table if not exists public.wrong_answers (
  id          bigint generated by default as identity primary key,
  user_name   text not null,
  group_id    text,
  game_id     text not null,
  q_key       text not null,
  q_text      text,
  correct     text,
  wrong       text,
  q_type      text,
  created_at  timestamptz not null default now()
);

create index if not exists wrong_answers_user_idx  on public.wrong_answers (user_name);
create index if not exists wrong_answers_group_idx on public.wrong_answers (group_id);

-- RLS: 익명 키로 insert/select/delete 허용 (scores 테이블과 동일 정책)
alter table public.wrong_answers enable row level security;

create policy "anon_insert_wrong" on public.wrong_answers for insert to anon with check (true);
create policy "anon_select_wrong" on public.wrong_answers for select to anon using (true);
create policy "anon_delete_wrong" on public.wrong_answers for delete to anon using (true);
